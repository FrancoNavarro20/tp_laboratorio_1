#include <stdio.h>
#include <stdlib.h>

/** \brief Pide dos numeros flotantes y realiza la suma de ellos;
 *
 * \param float primerNumero : es el primer numero ingresado;
 * \param float segundoNumero : es el segundo numero ingresado;
 * \return retorna la suma de primerNumero + segundoNumero;
 *
 */
float SumarFlotantes(float primerNumero, float segundoNumero);


/** \brief Pide dos numeros flotantes y realiza la resta de ellos;
 *
 * \param float primerNumero : es el primer numero ingresado;
 * \param float segundoNumero : es el segundo numero ingresado;
 * \return retorna la resta de primerNumero - segundoNumero;
 *
 */
float RestarFlotantes(float primerNumero, float segundoNumero);


/** \brief Pide dos numeros flotantes y realiza la division de ellos;
 *
 * \param float primerNumero : es el primer numero ingresado;
 * \param float segundoNumero : es el segundo numero ingresado;
 * \return retorna la division de primerNumero / segundoNumero;
 *
 */
float DividirFlotantes(float primerNumero, float segundoNumero);


/** \brief Pide dos numeros flotantes y realiza la multiplicacion de ellos;
 *
 * \param float primerNumero : es el primer numero ingresado;
 * \param float segundoNumero : es el segundo numero ingresado;
 * \return retorna la multiplicacion de primerNumero * segundoNumero;
 *
 */
float MultiplicacionFlotantes(float primerNumero, float segundoNumero);



/** \brief Pide solamente un numero double y realiza el factorial de ese numero;
 *
 * \param double Numero : es el numero ingresado;
 * \return retorna el factorial del numero;
 *
 */
double FactorialFlotante(double Numero);
